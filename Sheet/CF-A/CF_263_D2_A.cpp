#include<bits/stdc++.h>
using namespace std;

int main()
{
	int n = 5, val, r, c;

	for(int i = 1; i <= n; i++)
	{
		for(int j = 1; j <= n; j++)
		{
			cin>>val;
			if (val)
				r = i, c = j;
		}
	}

	cout<<abs(3 - r) + abs(3 - c);
	return 0;
}
