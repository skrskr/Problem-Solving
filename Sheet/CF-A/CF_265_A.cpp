#include<bits/stdc++.h>
using namespace std;

int main()
{
	string s, t;
	int cnt = 0;
	cin>>s>>t;
	int len = t.length();

	for(int i = 0; i < len; i++)
	{
		if(s[cnt] == t[i])
			cnt++;
	}

	cout<<(cnt + 1);
	return 0;
}
