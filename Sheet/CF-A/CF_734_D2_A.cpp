#include<bits/stdc++.h>
using namespace std;

int main()
{
	int n, a = 0, d = 0;
	string str;
	cin>>n;
	cin>>str;
	for(int i = 0; i < n; i++)
	{
		(str[i] == 'A') ? a++ : d++;
	}
	string res = (a == d) ? "Friendship" : (a > d) ? "Anton" : "Danik";
	cout<<res;
	return 0;
}
